QuadrangleTexel CDebugFont::createSprites(char s)
{
	QuadrangleTexel result = { { 0.0f, 1.0f },{ 0.0f, 0.0f },{ 1.0f, 1.0f },{ 1.0f, 0.0f } };
	for (size_t r = 0; r < gUV4_26.size(); r++)
	{
		if (s == gUV4_26[r].ch)
		{
			result = gUV4_26[r].coord;
		}
	}
	return result;
}

